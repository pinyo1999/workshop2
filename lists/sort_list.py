# Example 1
thislist = [100, 50, 65, 82, 23]
thislist.sort()
print(thislist)  # output : [23, 50, 65, 82, 100]

# Example 2
thislist = [100, 50, 65, 82, 23]
thislist.sort(reverse=True)
print(thislist)  # output : [100, 82, 65, 50, 23]

